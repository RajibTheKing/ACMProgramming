int revers(int a)
{
    stringstream ss;
    ss.clear();
    ss<<a;
    string s="";
    ss>>s;

    reverse(s.begin(), s.end());
    ss.clear();
    ss<<s;
    ss>>a;
    return a;

}
int rec(int a)
{
    if(a<=0) return INF;
    if(a==FinalValue)
    {
        return 0;
    }
    int ret;
    ret =  1 + rec(a/10);
    if(M[a]==false)
    {
        M[a]=true;
        ret = min(ret, 1+rec(revers(a)));

    }

    return ret;
}

